package Incapsulation;

public class User {
    String firtName;
    String lastName;
    String email;
    String phoneNumber;
    String password;

    public User(String firtName, String lastName, String email, String phoneNumber, String password) {
        this.firtName = firtName;
        this.lastName = lastName;
        this.email = email;
        this.phoneNumber = phoneNumber;
        this.password = password;
    }
}
